package model;

public class Food {
	private int food_id;
	private String food_name;
	private float food_price;
	private int food_num;
	private String food_dsp;
	private String food_format;
	
	public int getFood_id() {
		return food_id;
	}
	public void setFood_id(int food_id) {
		this.food_id = food_id;
	}
	public String getFood_name() {
		return food_name;
	}
	public void setFood_name(String food_name) {
		this.food_name = food_name;
	}
	public float getFood_price() {
		return food_price;
	}
	public void setFood_price(float food_price) {
		this.food_price = food_price;
	}
	public int getFood_num() {
		return food_num;
	}
	public void setFood_num(int food_num) {
		this.food_num = food_num;
	}
	public String getFood_dsp() {
		return food_dsp;
	}
	public void setFood_dsp(String food_dsp) {
		this.food_dsp = food_dsp;
	}
	public String getFood_format() {
		return food_format;
	}
	public void setFood_format(String food_format) {
		this.food_format = food_format;
	}
	
}
