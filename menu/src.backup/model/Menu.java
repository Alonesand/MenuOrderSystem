package model;

public class Menu {
	private int menu_id;
	private String menu_name;
	private String menu_pic;
	private String menu_user;
	private String menu_dsp;
	private float menu_grade;
	private int menu_collect;
	private int menu_look;
	public int getMenu_id() {
		return menu_id;
	}
	public void setMenu_id(int menu_id) {
		this.menu_id = menu_id;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public String getMenu_pic() {
		return menu_pic;
	}
	public void setMenu_pic(String menu_pic) {
		this.menu_pic = menu_pic;
	}
	public String getMenu_user() {
		return menu_user;
	}
	public void setMenu_user(String menu_user) {
		this.menu_user = menu_user;
	}
	public String getMenu_dsp() {
		return menu_dsp;
	}
	public void setMenu_dsp(String menu_dsp) {
		this.menu_dsp = menu_dsp;
	}
	public float getMenu_grade() {
		return menu_grade;
	}
	public void setMenu_grade(float menu_grade) {
		this.menu_grade = menu_grade;
	}
	public int getMenu_collect() {
		return menu_collect;
	}
	public void setMenu_collect(int menu_collect) {
		this.menu_collect = menu_collect;
	}
	public int getMenu_look() {
		return menu_look;
	}
	public void setMenu_look(int menu_look) {
		this.menu_look = menu_look;
	}
}
